	for(int i=0; i<len; i+=6) {
		for(int j=0; j<26; j++) {
			int flag = 1;
			for(int k=0; k<7; k++) {
				for(int l=0; l<5; l++) {
					if(b[k][i+l] != alphabet[j][k][l]) {
						flag = 0;
						break;
					}
				}
				if(!flag) break;
			}
			if(flag) {
				ans[cnt++] = (char)('A' + j);
				break;
			}
		}
	}

	printf("%s\n", ans);
	return 0;
